var pythonLang = LangConfig{
	Name:                      "python",
	DetectFiles:               []string{"requirements.txt", "pyproject.toml", "setup.py"},
	Extensions:                []string{".py"},
	AlreadyInstrumentedMarker: "opentelemetry",
	TelemetryFile:             "telemetry.py",
	TelemetryTemplate: `"""Orchestra OTel bootstrap — auto-generated by orchestra init --instrument."""
from opentelemetry import trace
from opentelemetry.exporter.otlp.proto.http.trace_exporter import OTLPSpanExporter
from opentelemetry.sdk.resources import Resource
from opentelemetry.sdk.trace import TracerProvider
from opentelemetry.sdk.trace.export import BatchSpanProcessor


def init_tracer(service_name: str) -> None:
    """Configure OTLP/HTTP exporter pointing at localhost:4318 (orchestra runtime serve)."""
    resource = Resource.create({"service.name": service_name})
    provider = TracerProvider(resource=resource)
    exporter = OTLPSpanExporter(endpoint="http://localhost:4318/v1/traces")
    provider.add_span_processor(BatchSpanProcessor(exporter))
    trace.set_tracer_provider(provider)
`,
	InstallCmd:  "pip",
	InstallArgs: []string{"install", "opentelemetry-sdk", "opentelemetry-exporter-otlp-proto-http"},
	MainPatch: MainPatch{
		EntryGlobs:  []string{"main.py", "app.py", "run.py", "__main__.py"},
		InsertAfter: "if __name__ == \"__main__\":",
		InitCall:    "\n    from telemetry import init_tracer; init_tracer(\"{{SERVICE}}\")\n",
		ImportLine:  "",
	},
}

