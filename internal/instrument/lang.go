// ---- Go ----

var goLang = LangConfig{
	Name:                      "go",
	DetectFiles:               []string{"go.mod"},
	Extensions:                []string{".go"},
	AlreadyInstrumentedMarker: "go.opentelemetry.io/otel",
	TelemetryFile:             "internal/telemetry/otel.go",
	TelemetryTemplate: `package telemetry

import (
	"context"
	"log"

	"go.opentelemetry.io/otel"
	"go.opentelemetry.io/otel/exporters/otlp/otlptrace/otlptracehttp"
	"go.opentelemetry.io/otel/sdk/resource"
	sdktrace "go.opentelemetry.io/otel/sdk/trace"
	semconv "go.opentelemetry.io/otel/semconv/v1.26.0"
)

// InitTracer initialises an OTLP/HTTP trace exporter pointing at localhost:4318
// (the address used by orchestra runtime serve).
// Call it at the very start of main() and defer the returned shutdown function:
//
//	defer telemetry.InitTracer("my-service")()
func InitTracer(serviceName string) func() {
	exp, err := otlptracehttp.New(context.Background(),
		otlptracehttp.WithEndpoint("localhost:4318"),
		otlptracehttp.WithInsecure(),
	)
	if err != nil {
		log.Fatalf("otlp exporter: %v", err)
	}
	tp := sdktrace.NewTracerProvider(
		sdktrace.WithBatcher(exp),
		sdktrace.WithResource(resource.NewWithAttributes(
			semconv.SchemaURL,
			semconv.ServiceName(serviceName),
		)),
	)
	otel.SetTracerProvider(tp)
	return func() { _ = tp.Shutdown(context.Background()) }
}
`,
	InstallCmd:  "go",
	InstallArgs: []string{"get", "go.opentelemetry.io/otel/exporters/otlp/otlptrace/otlptracehttp"},
	MainPatch: MainPatch{
		EntryGlobs:  []string{"main.go", "cmd/*/main.go", "cmd/main.go"},
		InsertAfter: "func main() {",
		InitCall:    "\n\tdefer telemetry.InitTracer(\"{{SERVICE}}\")()  // orchestra: OTel tracing\n",
		ImportLine:  "{{MODULE}}/internal/telemetry",
	},
}

