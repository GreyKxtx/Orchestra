var tsLang = LangConfig{
	Name:                      "typescript",
	DetectFiles:               []string{"tsconfig.json"},
	Extensions:                []string{".ts"},
	AlreadyInstrumentedMarker: "@opentelemetry",
	TelemetryFile:             "telemetry.ts",
	TelemetryTemplate: `// Orchestra OTel bootstrap — auto-generated by orchestra init --instrument.
// Load BEFORE your application code via:
//   ts-node -r ./telemetry.ts src/index.ts
//   node --require ./telemetry.js dist/index.js   (after tsc)
import { NodeSDK } from "@opentelemetry/sdk-node";
import { OTLPTraceExporter } from "@opentelemetry/exporter-trace-otlp-http";
import { Resource } from "@opentelemetry/resources";
import { SemanticResourceAttributes } from "@opentelemetry/semantic-conventions";

const sdk = new NodeSDK({
  resource: new Resource({
    [SemanticResourceAttributes.SERVICE_NAME]: process.env.OTEL_SERVICE_NAME ?? "{{SERVICE}}",
  }),
  traceExporter: new OTLPTraceExporter({
    url: "http://localhost:4318/v1/traces",
  }),
});
sdk.start();
process.on("SIGTERM", () => sdk.shutdown().finally(() => process.exit(0)));
`,
	InstallCmd: "npm",
	InstallArgs: []string{
		"install",
		"@opentelemetry/sdk-node",
		"@opentelemetry/exporter-trace-otlp-http",
		"@opentelemetry/resources",
		"@opentelemetry/semantic-conventions",
	},
	// TS/JS: loaded via --require, no code patching.
	MainPatch: MainPatch{},
}

